package ie.bookeo.model.drive;

public class DriveFolder {

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public DriveFolder() {
    }

    String id;
    String name;
    String path;

}
